// Función para obtener el icono y color de un servicio
export function getServiceIcon(serviceName) {
  if (!serviceName) {
    return null;
  }

  // Normalizar el nombre (lowercase, trim)
  const normalized = serviceName.toLowerCase().trim();
  
  // Buscar coincidencia exacta
  if (SERVICE_ICONS[normalized]) {
    return SERVICE_ICONS[normalized];
  }
  
  // Buscar coincidencia parcial (si el nombre contiene alguna palabra clave)
  for (const [key, value] of Object.entries(SERVICE_ICONS)) {
    if (normalized.includes(key) || key.includes(normalized)) {
      return value;
    }
  }
  
  // Si no hay coincidencia, retornar null (usar método actual)
  return null;
}

// Función para generar icono y color por defecto basado en el nombre
export function generateDefaultIcon(name) {
  if (!name) {
    return { icon: null, fallback: '?', color: '#6366f1', isUrl: false };
  }
  
  // Usar la primera letra en mayúscula como fallback
  const fallback = name.charAt(0).toUpperCase();
  
  // Generar un color basado en el nombre (hash simple)
  const colors = [
    '#8b5cf6', '#6366f1', '#3b82f6', '#0ea5e9', '#06b6d4',
    '#14b8a6', '#10b981', '#84cc16', '#eab308', '#f59e0b',
    '#f97316', '#ef4444', '#ec4899', '#d946ef', '#a855f7'
  ];
  
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = name.charCodeAt(i) + ((hash << 5) - hash);
  }
  
  const color = colors[Math.abs(hash) % colors.length];
  
  return { icon: null, fallback, color, isUrl: false };
}

// Función principal para obtener icono y color (con fallback)
export function getIconAndColor(name, currentLogo, currentColor) {
  // Si ya tiene logo personalizado (no es URL ni primera letra), mantenerlo
  if (currentLogo && currentColor && 
      !currentLogo.startsWith('http') && 
      currentLogo !== name.charAt(0).toUpperCase()) {
    return { icon: currentLogo, fallback: currentLogo, color: currentColor, isUrl: false };
  }
  
  // Intentar obtener icono del servicio conocido
  const serviceIcon = getServiceIcon(name);
  if (serviceIcon) {
    return { 
      icon: serviceIcon.icon, 
      fallback: serviceIcon.fallback, 
      color: serviceIcon.color, 
      isUrl: true 
    };
  }
  
  // Generar icono por defecto
  return generateDefaultIcon(name);
}

